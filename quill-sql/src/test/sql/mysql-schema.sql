CREATE TABLE Person(
	name varchar(255),
	age int(3)
);

CREATE TABLE Couple(
	her varchar(255),
	him varchar(255)
);

CREATE TABLE Department(
	dpt varchar(255)
);

CREATE TABLE Employee(
	emp varchar(255),
	dpt varchar(255),
	salary int(20)
);

CREATE TABLE Task(
	emp varchar(255),
	tsk varchar(255)
);
